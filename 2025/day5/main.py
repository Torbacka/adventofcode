from collections import defaultdict
input = """
123 328  51 64
 45 64  387 23
  6 98  215 314
*   +   *   +
"""
numbers_lines = [line for line in input]
numbers_dict = defaultdict(list)
for line in numbers_lines:
    for i, number in enumerate(line.split(" ")):
        numbers_dict[i].append(int(number))

